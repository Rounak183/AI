go_bfs(Start,Goal) :-     
	empty_set(Closed_set),     
	empty_pq(Open),     
	heuristic(Start,Goal,H),
	insert_pq([Start,nil,0,H,H],Open,Open_pq),
	path_bfs(Open_pq, Closed_set, Goal).

path_bfs(Open_pq, _,_) :-     
	empty_pq(Open_pq),
	write('Goal Test failed'),nl,
	write('Graph searched, no solution found.').

path_bfs(Open_pq,Closed_set,Goal):- 
	dequeue_pq([State,Parent,_,_,_],Open_pq,_),     
	State=Goal,
	write('Goal Test is Passed'),nl,
	write('The solution path is: '),nl,
	printsolution_bfs([State,Parent,_,_,_],Closed_set,0,0,Goal).
	%write('Cost of the path traversed is: '),nl
	%write(Cost),nl.

path_bfs(Open_pq,Closed_set,Goal):-
	dequeue_pq([State,Parent,D,H,S],Open_pq,Rest_open_pq),
	get_children_bfs([State, Parent, D, H, S],Rest_open_pq, Closed_set, Children,Goal),
	insert_list_pq(Children,Rest_open_pq,New_open_pq),
	write('The new open state is: '),nl,
	writelist(New_open_pq),
	write(''),nl,
	union_bfs([[State, Parent, D, H, S]],Closed_set,New_closed_set),
	write('The new closed state is: '),nl,
	writelist(New_closed_set),
	write(''),nl,
	path_bfs(New_open_pq,New_closed_set,Goal),!.

get_children_bfs([State,_,D,_, _],Rest_open_pq,Closed_set,Children,Goal):-
	bagof(Child,moves_bfs([State,_,D,_,_],Rest_open_pq,Closed_set,Child,Goal),Children).

get_children_bfs([State,_,D,_, _],Rest_open_pq,Closed_set,Children,Goal):-
	not(bagof(Child,moves_bfs([State,_,D,_,_],Rest_open_pq,Closed_set,Child,Goal),Children)).

moves_bfs([State,_,Depth,_,_],Rest_open_pq,Closed_set,[Next,State,New_D,H,S],Goal):-
	move(State,Next,_), 
	not(member_pq([Next,_,_,_,_],Rest_open_pq)),
	not(member_set_bfs([Next,_,_,_,_],Closed_set)),     
	New_D is Depth+1,     
	heuristic(Next, Goal, H),
	S is New_D+H.

printsolution_bfs([State,nil,_,_,_],_,PathCost,HeuristicCost,Goal):-
	write('The Total Best First Search Cost is: '),
	write(PathCost),nl,	
	write('The Total Heuristic Cost is: '),
	write(HeuristicCost),nl,
	write(State),nl.

printsolution_bfs([State,Parent,_,_,_],Closed_set,PathCost,HeuristicCost,Goal):-
	member_set_bfs([Parent,Grandparent,_,_,_],Closed_set),
	move(State,Parent,PathCostNew),
	heuristic(Parent,Goal,HeuristicCostNew),
	PathCost2 is PathCost+PathCostNew,
	HeuristicCost2 is HeuristicCost+HeuristicCostNew,
	printsolution_bfs([Parent,Grandparent,_,_,_],Closed_set,PathCost2,HeuristicCost2,Goal),
	write(State),nl,
	move(State,Parent,PathCostNew),
	heuristic(Parent,Goal,HeuristicCostNew),
	write('The Path cost between '),
	write(State),
	write(' and '),
	write(Parent),
	write(' is: '),
	write(PathCostNew),nl,
	write('The Heuristic cost between '),
	write(Parent),
	write(' and '),
	write(Goal),
	write(' is: '),
	write(HeuristicCostNew),nl.

empty_set([]).
empty_pq([]).

dequeue_pq(E, [E | T], T).
dequeue_pq(E, [E | T], _).

insert_pq(State,[],[State]):-!.
insert_pq(State,[H|Tail],[State,H|Tail]):-
	precedes(State,H).
insert_pq(State,[H|T],[H|Tnew]):-
	insert_pq(State,T,Tnew).
precedes([_,_,_,H1,_],[_,_,_,H2,_]):- 
	H1 < H2.

insert_list_pq([],L,L).
insert_list_pq([State|Tail],L,New_L):-
	insert_pq(State,L,L2),
	insert_list_pq(Tail,L2,New_L).

union_bfs([],S,S).
union_bfs([H|T],S,S_new):-
	union_bfs(T,S,S2),
	add_if_not_in_set(H,S2,S_new),!.
add_if_not_in_set(X,S,S):-
	member(X,S),!.
add_if_not_in_set(X,S,[X|S]).

member_pq(Element, Stack):-
	member(Element, Stack).
member_set_bfs(E, S):-
	member(E, S).

writelist([]).
writelist([H|T]):-
	write(H),nl,
	writelist(T).
